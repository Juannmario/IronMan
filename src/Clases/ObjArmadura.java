
package Clases;


public abstract class ObjArmadura {
    public float consumo;

    public ObjArmadura() {
        this.consumo = 0;
    }

    public float getConsumo() {
        return consumo;
    }

    public void setConsumo(float consumo) {
        this.consumo = consumo;
    }
    
   
    
}
